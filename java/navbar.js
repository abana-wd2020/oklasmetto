function openNav() {
  document.getElementById("mob_nav").style.width = "100%";
  document.getElementById("nav_button").style.visibility = "hidden";
}

function closeNav() {
  document.getElementById("mob_nav").style.width = "0%";
  document.getElementById("nav_button").style.visibility = "visible";
}


